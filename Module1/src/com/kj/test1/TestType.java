package com.kj.test1;

public class TestType {
    public static void main(String[] args) {
        // primitive data types
        // int type
        byte a = 10; // -128 to 127
        short b = 100; // -32768 to 32767
        int c = 1000; // -2147483648 to 2147483647
        long d = 10000000000L; // -9223372036854775808 to 9223372036854775807

        // float type
        float e = 3.14f;
        double f = 3.1415926;

        // char type
        char g = 'A'; // single quote for char, double quote for string

        // string type
        String str = "Hello World!";

        // boolean type
        boolean h = true;

    }
}
